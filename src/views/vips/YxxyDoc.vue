<p><span style="color:rgb(0,0,0);font-size:11pt;">第十二章 法律文书送达</span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;"> </span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;">第四十四条各方约定，用户在本协议或羊习习羊中写明的手机号码、电子邮箱以及其他登记在用户名下的手机号码等联系方式，均为各方之间纠纷相关材料有效送达地址。前述地址适用于本协议成立时起至债务全部履行完毕时止，包括适用于仲裁阶段及执行阶段。如一方需变更电子邮箱或者手机号码的，应当以书面通知并得到对方确认，否则短信、电子邮件等发送原约定邮箱或手机号码，发出即视为送达。</span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;"> </span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;">第四十五条用户本人指定接收法律文书的手机号码或者电子邮箱为合同约定的或注册账户绑定的手机号码或者电子邮箱，司法机关向该手机号码或者电子邮箱或用户指定邮寄地址发出法律文书即视为送达。</span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;"> </span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;">第四十六条用户本人指定邮寄地址为本人在平台方或注册钱橙无忧时使用的户籍地址或者身份证地址。</span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;"> </span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;">第四十七条用户同意司法机关可采取以上一种或多种送达方式向本人送达法律文书，司法机关采取多种方式向本人送达法律文书，送达时间以上述送方式中最先送达的为准。</span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;"> </span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;">第四十八条用户确认的上述送达方式适用于各个司法阶段，包括但不限于仲裁、执行以及督促程序（含支付令送达）。</span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;"> </span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;">第四十九条若用户本人上述送达地址有变更，本人应当及时告知羊习习羊、平台方和司法机关（如适用）变更后的送达地址。</span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;"> </span></p>

<p><span style="color:rgb(0,0,0);font-size:11pt;">第五十条用户保证上述送达地址是准确的、有效的；如果提供的送达地址不确切或不及时告知变更后的地址，使法律文书无法送达或未及时送达，用户本人自行承担由此产生的法律后果。</span></p>

